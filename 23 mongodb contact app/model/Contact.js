const mongoose = require("mongoose");
mongoose.connect("mongodb://localhost:27017/tes1");

const Contact = mongoose.model("Contact", {
  nama: {
    type: String,
    required: true,
  },
  nohp: {
    type: String,
    required: true,
  },
  email: {
    type: String,
  },
});

module.exports = Contact;